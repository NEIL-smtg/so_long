#include "../libft.h"

int	ft_atoi(const char *str)
{
	int	sign;
	int	i;
	int	total;

	sign = 1;
	i = 0;
	total = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '+')
		i++;
	else if (str[i] == '-')
	{
		sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		total = total * 10 + str[i] - '0';
		i++;
	}
	return (total * sign);
}

